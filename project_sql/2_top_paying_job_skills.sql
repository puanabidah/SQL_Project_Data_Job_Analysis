/*
Question: What are the skills required for these top_paying roles?
- Use the top 10 highest-paying data analyst jobs from first query.
- Add the specific skills required for these roles.
- Why? It provides a detailed look at which high paying jobs demand certain skills, helping job seekers undertand which skills to develop that align with top salaries.
*/

WITH top_paying_jobs AS(
    SELECT
        job_id,
        job_title,
        name AS company_name,
        salary_year_avg
    FROM job_postings_fact AS jp
    LEFT JOIN company_dim AS cd
        ON jp.company_id = cd.company_id
    WHERE job_title_short = 'Data Analyst' AND
        job_location = 'Anywhere' AND
        salary_year_avg IS NOT NULL
    ORDER BY salary_year_avg DESC
    LIMIT 10
)

SELECT
    tpj.*,
    skills AS skills_required
FROM top_paying_jobs AS tpj
INNER JOIN skills_job_dim AS sj
    ON tpj.job_id = sj.job_id
INNER JOIN skills_dim AS sd
    ON sj.skill_id = sd.skill_id
ORDER BY salary_year_avg DESC;

